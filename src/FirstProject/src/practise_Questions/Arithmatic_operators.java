//Create three static methods to perform:Addition;Subtraction;MultiplicationCall; all methods inside main.
public class Arithmatic_operators {
   static void add()
   {
	   int a = 10;
	   int b = 20;
	   int c = a+b;
	   System.out.println(c);
   }
   static void Sub()
   {
	  double a = 1.25;
	  double b= 2.25;
	   double c = b-a;
	   System.out.println(c);
   }
   static void mul()
   {
	  double a = 1.25;
	  double b= 2.25;
	   double c=a*b;
	   System.out.println(c);
   }
	
	public static void main(String[] args) {
		add();
		Sub();
		mul();

	}

}
