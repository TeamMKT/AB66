package package1;
//"Write A Program to print -5 to -16 and  16 to 5 using for loop"
public class Assignment_46 {

	public static void main(String[] args) 
	{
		for(int i=-5;i>=-16;i--)
		{
			System.out.println(i);
		}
        for (int i=16;i>=5;i--)
        {
        	System.out.println(i);
        }
	}

}
