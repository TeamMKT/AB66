package package1;

public class Ifelse_block2 
{

	public static void main(String[] args) 
	{
		int age=10;
		if(age >=18)
		{
			System.out.println("you can vote");
		}
		else
		{
			System.out.println("can't vote");
		}
	}

}
/*IF condition is true the block will execute and if condition is false then else block will execute.
Here, the condition is false so else block is executed.*/