		System.out.println(c); //utilization
		
		long d ; //decaration
		d=987654321234l; // initialization
		System.out.println(d); //utilization
		
		float e ; //decaration
		e=25.6345f;  // initialization
		System.out.println(e); //utilization
		
		double f ; //decaration 
		f=3.2514585674; // initialization
		System.out.println(f); //utilization
		
		char g ; //decaration
