package package1;

public class DatatypesAndVariables {

	public static void main(String[] args)
	{
		byte a=100;//declaration and initialization, here a is a variable declaring the datatype byte called declaration and initializing with a value 100 is called initialization.
		System.out.println(a); //utilization
		short b=-25000;
		System.out.println(b);
		int c=987564325;
		System.out.println(c);
		long d=987654321234l;//for long enter l at the end of the number(suffix) otherwise it shows compilation error.
		System.out.println(d);
		float e=25.6345f;
		System.out.println(e);
		double f=3.2514585674; //in double means after decimal place 17 to 19 numbers will be allowed.
		System.out.println(f);
		char g='c'; // char must be in single quotation it might be capital or small letter.
		System.out.println(g);
		boolean answer=true;  // boolean will store just true or false, true or false muse be write in lower case.
		System.out.println(answer);
		String name="Geeta";
		System.out.println(name);
	}

}
